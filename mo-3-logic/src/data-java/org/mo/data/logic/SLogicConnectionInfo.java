package org.mo.data.logic;

//============================================================
// <T>逻辑链接描述。</T>
//============================================================
public class SLogicConnectionInfo
{
   // 名称
   protected String _name;

   //============================================================
   // <T>构造逻辑表。</T>
   //============================================================
   public SLogicConnectionInfo(){
   }

   //============================================================
   // <T>构造逻辑表。</T>
   //
   // @param name 字段名称
   //============================================================
   public SLogicConnectionInfo(String name){
      _name = name;
   }

   //============================================================
   // <T>获得名称。</T>
   //
   // @param name 名称
   //============================================================
   public String name(){
      return _name;
   }

   //============================================================
   // <T>获得小写名称。</T>
   //
   // @param name 名称
   //============================================================
   public String lowerName(){
      return _name.toLowerCase();
   }

   //============================================================
   // <T>设置名称。</T>
   //
   // @param name 名称
   //============================================================
   public void setName(String name){
      _name = name;
   }

   //============================================================
   // <T>获得字符串。</T>
   //
   // @return 字符串
   //============================================================
   @Override
   public String toString(){
      return _name;
   }
}
