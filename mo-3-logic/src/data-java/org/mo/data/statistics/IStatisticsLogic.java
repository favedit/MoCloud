package org.mo.data.statistics;

import org.mo.data.face.FDomainStatisticsLogicUnit;

//============================================================
// <T>逻辑统计接口。</T>
//============================================================
public interface IStatisticsLogic
{
   //============================================================
   // <T>获得名称。</T>
   //
   // @return 名称
   //============================================================
   String name();

   //============================================================
   // <T>设置名称。</T>
   //
   // @param name 名称
   //============================================================
   void setName(String name);

   //============================================================
   // <T>获得回调名称。</T>
   //
   // @return 回调名称
   //============================================================
   String invokeName();

   //============================================================
   // <T>设置回调名称。</T>
   //
   // @param invokeName 回调名称
   //============================================================
   void setInvokeName(String invokeName);

   //============================================================
   // <T>获得数据统计组。</T>
   //
   // @return 数据统计组
   //============================================================
   FStatistics statistics();

   //============================================================
   // <T>设置数据统计组。</T>
   //
   // @param statistics 数据统计组
   //============================================================
   void setStatistics(FStatistics statistics);

   //============================================================
   // <T>获得数据单元。</T>
   //
   // @return 数据单元
   //============================================================
   FDomainStatisticsLogicUnit unit();

   //============================================================
   // <T>设置数据单元。</T>
   //
   // @param unit 数据单元
   //============================================================
   void setUnit(FDomainStatisticsLogicUnit unit);

   //============================================================
   // <T>获得处理条数。</T>
   //
   // @return 处理条数
   //============================================================
   int processCount();

   //============================================================
   // <T>获得线程。</T>
   //
   // @return 线程
   //============================================================
   FStatisticsLogicThread thread();

   //============================================================
   // <T>计算处理。</T>
   //
   // @return 统计结果
   //============================================================
   EStatisticsResult calculate();

   //============================================================
   // <T>启动处理。</T>
   //============================================================
   void startup();

   //============================================================
   // <T>逻辑处理。</T>
   //
   // @return 统计结果
   //============================================================
   EStatisticsResult process();

   //============================================================
   // <T>关闭处理。</T>
   //============================================================
   void shutdown();
}
