package org.mo.web.protocol.context;

import org.mo.com.lang.FAttributes;
import org.mo.com.xml.FXmlNode;

//============================================================
// <T>网页配置输入器接口。</T>
//============================================================
public interface IWebInput
{
   //============================================================
   // <T>获得设置节点。</T>
   //
   // @return 设置节点
   //============================================================
   FXmlNode config();

   //============================================================
   // <T>获得类型。</T>
   //
   // @return 类型
   //============================================================
   String type();

   //============================================================
   // <T>获得设置内容。</T>
   //
   // @param name 名称
   // @return 设置内容
   //============================================================
   String get(String name);

   //============================================================
   // <T>获得参数集合。</T>
   //
   // @return 参数集合
   //============================================================
   FAttributes parameters();

   //============================================================
   // <T>获得参数内容。</T>
   //
   // @param name 名称
   // @return 参数内容
   //============================================================
   String parameter(String name);
}
