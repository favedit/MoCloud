package org.mo.web.core.action.common;

//============================================================
// <T>COOKIE。</T>
//============================================================
public interface IWebCookie
{
   //============================================================
   // <T>获得名称。</T>
   //
   // @return 名称
   //============================================================
   String name();

   //============================================================
   // <T>获得内容。</T>
   //
   // @return 内容
   //============================================================
   String value();

   //============================================================
   // <T>获得期限。</T>
   //
   // @return 期限
   //============================================================
   int expiry();
}
