package org.mo.web.core.webform.control;

//============================================================
// <T>工具按钮接口对象的XML节点基类。</T>
//============================================================
public interface XToolButtonFace
      extends
         XControlFace
{
   // 名称定义
   String NAME = "MToolButton";

   // 图标的名称定义
   String PTY_ICON = "icon";

   // 禁止图标的名称定义
   String PTY_ICON_DISABLE = "icon_disable";

   // 热键的名称定义
   String PTY_HOTKEY = "hotkey";

   // 目标页面的名称定义
   String PTY_TARGET = "target";

   // 命令的名称定义
   String PTY_ACTION = "action";

   // 服务的名称定义
   String PTY_SERVICE = "service";

   // 页面的名称定义
   String PTY_PAGE = "page";

   // 属性集合的名称定义
   String PTY_ATTRIBUTES = "attributes";

   // 数据命令的名称定义
   String PTY_DATA_ACTION = "data_action";

   //============================================================
   // <T>获得图标的内容。</T>
   //
   // @return 图标
   //============================================================
   String getIcon();

   //============================================================
   // <T>设置图标的内容。</T>
   //
   // @param value 图标
   //============================================================
   void setIcon(String value);

   //============================================================
   // <T>获得禁止图标的内容。</T>
   //
   // @return 禁止图标
   //============================================================
   String getIconDisable();

   //============================================================
   // <T>设置禁止图标的内容。</T>
   //
   // @param value 禁止图标
   //============================================================
   void setIconDisable(String value);

   //============================================================
   // <T>获得热键的内容。</T>
   //
   // @return 热键
   //============================================================
   String getHotkey();

   //============================================================
   // <T>设置热键的内容。</T>
   //
   // @param value 热键
   //============================================================
   void setHotkey(String value);

   //============================================================
   // <T>获得目标页面的内容。</T>
   //
   // @return 目标页面
   //============================================================
   String getTarget();

   //============================================================
   // <T>设置目标页面的内容。</T>
   //
   // @param value 目标页面
   //============================================================
   void setTarget(String value);

   //============================================================
   // <T>获得命令的内容。</T>
   //
   // @return 命令
   //============================================================
   String getAction();

   //============================================================
   // <T>设置命令的内容。</T>
   //
   // @param value 命令
   //============================================================
   void setAction(String value);

   //============================================================
   // <T>获得服务的内容。</T>
   //
   // @return 服务
   //============================================================
   String getService();

   //============================================================
   // <T>设置服务的内容。</T>
   //
   // @param value 服务
   //============================================================
   void setService(String value);

   //============================================================
   // <T>获得页面的内容。</T>
   //
   // @return 页面
   //============================================================
   String getPage();

   //============================================================
   // <T>设置页面的内容。</T>
   //
   // @param value 页面
   //============================================================
   void setPage(String value);

   //============================================================
   // <T>获得属性集合的内容。</T>
   //
   // @return 属性集合
   //============================================================
   String getAttributes();

   //============================================================
   // <T>设置属性集合的内容。</T>
   //
   // @param value 属性集合
   //============================================================
   void setAttributes(String value);

   //============================================================
   // <T>获得数据命令的内容。</T>
   //
   // @return 数据命令
   //============================================================
   String getDataAction();

   //============================================================
   // <T>设置数据命令的内容。</T>
   //
   // @param value 数据命令
   //============================================================
   void setDataAction(String value);
}
