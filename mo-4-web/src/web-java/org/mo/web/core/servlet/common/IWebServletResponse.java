package org.mo.web.core.servlet.common;

//============================================================
// <T>网页应答接口。</T>
//============================================================
public interface IWebServletResponse
{
   //============================================================
   // <T>设置容量长度。</T>
   //
   // @param length 长度
   //============================================================
   void setContentLength(int length);

   //============================================================
   // <T>设置编码形式。</T>
   //
   // @param encoding 编码形式
   //============================================================
   void setCharacterEncoding(String encoding);

   //============================================================
   // <T>设置内容类型。</T>
   //
   // @param contentType 内容类型
   //============================================================
   void setContentType(String contentType);

   //============================================================
   // <T>设置状态。</T>
   //
   // @param value 内容
   //============================================================
   void setStatus(int value);

   //============================================================
   // <T>增加一个头信息。</T>
   //
   // @param name 名称
   // @param value 内容
   //============================================================
   void addHeader(String name,
                  int value);

   //============================================================
   // <T>增加一个头信息。</T>
   //
   // @param name 名称
   // @param value 内容
   //============================================================
   void addHeader(String name,
                  long value);

   //============================================================
   // <T>增加一个头信息。</T>
   //
   // @param name 名称
   // @param value 内容
   //============================================================
   void addHeader(String name,
                  String value);

   //============================================================
   // <T>设置一个头信息。</T>
   //
   // @param name 名称
   // @param value 内容
   //============================================================
   void setHeader(String name,
                  int value);

   //============================================================
   // <T>设置一个头信息。</T>
   //
   // @param name 名称
   // @param value 内容
   //============================================================
   void setHeader(String name,
                  long value);

   //============================================================
   // <T>设置一个头信息。</T>
   //
   // @param name 名称
   // @param value 内容
   //============================================================
   void setHeader(String name,
                  String value);

   //============================================================
   // <T>写入数据。</T>
   //
   // @param data 数据内容
   //============================================================
   void write(byte[] data);

   //============================================================
   // <T>写入数据。</T>
   //
   // @param data 数据内容
   // @param offset 数据位置
   // @param length 数据长度
   //============================================================
   void write(byte[] data,
              int offset,
              int length);

   //============================================================
   // <T>刷新数据。</T>
   //============================================================
   void flush();
}
