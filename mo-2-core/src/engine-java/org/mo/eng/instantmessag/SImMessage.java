package org.mo.eng.instantmessag;

//============================================================
// <T>消息参数集合。</T>
//============================================================
public class SImMessage
{
   // 账号
   protected String _passport;

   // 消息
   protected String _text;

   //============================================================
   // <T>构造消息参数集合。</T>
   //============================================================
   public SImMessage(){
   }

   //============================================================
   // <T>获得账号。</T>
   //
   // @return 账号
   //============================================================
   public String passport(){
      return _passport;
   }

   //============================================================
   // <T>设置账号。</T>
   //
   // @param passport 账号
   //============================================================
   public void setPassport(String passport){
      _passport = passport;
   }

   //============================================================
   // <T>获得文本。</T>
   //
   // @return 文本
   //============================================================
   public String text(){
      return _text;
   }

   //============================================================
   // <T>设置文本。</T>
   //
   // @param text 文本
   //============================================================
   public void setText(String text){
      _text = text;
   }
}
