package org.mo.eng.session.common;

import java.io.Serializable;
import org.mo.com.collections.FObjectDictionary;
import org.mo.com.lang.FAttributes;
import org.mo.com.lang.cultrue.FCulture;
import org.mo.eng.security.IPermission;

//============================================================
// <T>会话接口。</T>
//============================================================
public interface ISession
      extends
         Serializable
{
   //============================================================
   // <T>获得编号。</T>
   //
   // @return 编号
   //============================================================
   String id();

   //============================================================
   // <T>获得存储编号。</T>
   //
   // @return 存储编号
   //============================================================
   String storeId();

   //============================================================
   // <T>获得语言编号。</T>
   //
   // @return 语言编号
   //============================================================
   String languageId();

   //============================================================
   // <T>设置语言编号。</T>
   //
   // @param languageId 语言编号
   //============================================================
   void setLanguageId(String languageId);

   //============================================================
   // <T>获得属性字典。</T>
   //
   // @return 属性字典
   //============================================================
   FObjectDictionary attributes();

   //============================================================
   // <T>获得参数字典。</T>
   //
   // @return 参数字典
   //============================================================
   FAttributes parameters();

   //============================================================
   // <T>获得权限对象。</T>
   //
   // @return 权限对象
   //============================================================
   IPermission permission();

   //============================================================
   // <T>获得组件字典。</T>
   //
   // @return 组件字典
   //============================================================
   FObjectDictionary componments();

   //============================================================
   // <T>获得文化设置。</T>
   //
   // @return 文化设置
   //============================================================
   FCulture culture();

   //============================================================
   // <T>测试是否被引用。</T>
   //
   // @return 是否被引用
   //============================================================
   boolean referTest();

   //============================================================
   // <T>测试引用计数加一。</T>
   //============================================================
   void referIncrease();

   //============================================================
   // <T>测试引用计数减一。</T>
   //============================================================
   void referDecrease();
}
