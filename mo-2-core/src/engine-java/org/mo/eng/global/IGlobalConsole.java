package org.mo.eng.global;

//============================================================
// <T>全局管理器接口。</T>
//============================================================
public interface IGlobalConsole
{
   //============================================================
   // <T>根据名称查找一个全局对象。</T>
   //
   // @param name 名称
   // @return 全局对象
   //============================================================
   <V> V find(String name);

   //============================================================
   // <T>根据名称查找一个全局对象。</T>
   //
   // @param clazz 类对象
   // @return 全局对象
   //============================================================
   <V> V find(Class<?> clazz);

   //============================================================
   // <T>根据名称查找一个全局对象。</T>
   //
   // @param name 名称
   // @param clazz 类对象
   // @return 全局对象
   //============================================================
   <V> V find(String name,
              Class<?> clazz);

   //============================================================
   // <T>根据名称移除一个全局对象。</T>
   //
   // @param name 名称
   //============================================================
   void remove(String name);

   //============================================================
   // <T>根据名称移除一个全局对象。</T>
   //
   // @param clazz 类对象
   //============================================================
   void remove(Class<?> clazz);

   //============================================================
   // <T>清空所有内容。</T>
   //============================================================
   void clear();
}
