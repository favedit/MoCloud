package org.mo.core.bind;

import org.mo.com.lang.FDictionary;

//============================================================
// <T>绑定信息。</T>
//============================================================
public class FBindInfo
      extends FDictionary<Object>
{
   // 代码
   protected long _code;

   //============================================================
   // <T>构造绑定信息。</T>
   //============================================================
   public FBindInfo(){
      super(Object.class);
   }

   //============================================================
   // <T>获得代码。</T>
   //
   // @return 代码
   //============================================================
   public long code(){
      return _code;
   }

   //============================================================
   // <T>设置代码。</T>
   //
   // @param code 代码
   //============================================================
   public void setCode(long code){
      _code = code;
   }

   //============================================================
   // <T>释放所有对象。</T>
   //============================================================
   public void release(){
   }
}
