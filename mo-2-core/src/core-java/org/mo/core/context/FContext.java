package org.mo.core.context;

//============================================================
// <T>上下文。</T>
//============================================================
public class FContext
      implements
         IContext
{
   // 代码
   protected String _code;

   //============================================================
   // <T>构造上下文。</T>
   //============================================================
   public FContext(){
   }

   //============================================================
   // <T>获得代码。</T>
   //
   // @return 代码
   //============================================================
   @Override
   public String code(){
      return _code;
   }

   //============================================================
   // <T>设置代码。</T>
   //
   // @param code 代码
   //============================================================
   public void setCode(String code){
      _code = code;
   }

   //============================================================
   // <T>释放资源。</T>
   //============================================================
   @Override
   public void release(){
   }
}
