package org.mo.core.monitor.common;

import org.mo.com.lang.FError;

//============================================================
// <T>监视器接口。</T>
//============================================================
public interface IMonitor
{
   // 名称
   String NAME = "Monitor";

   //============================================================
   // <T>获得分组名称。</T>
   //
   // @return 分组名称
   //============================================================
   String groupName();

   //============================================================
   // <T>获得名称。</T>
   //
   // @return 名称
   //============================================================
   String name();

   //============================================================
   // <T>获得有效性。</T>
   //
   // @return 有效性
   //============================================================
   boolean valid();

   //============================================================
   // <T>初始化监视器。</T>
   //============================================================
   void initialize();

   //============================================================
   // <T>判断是否可以执行逻辑。</T>
   //
   // @return TRUE：是<br>FALSE：否 
   //============================================================
   boolean runable();

   //============================================================
   // <T>执行监视器的逻辑。</T>
   //============================================================
   boolean onExecute();

   //============================================================
   // <T>例外处理。</T>
   //
   // @param exception 例外对象
   //============================================================
   void onError(FError exception);

   //============================================================
   // <T>挂起处理。</T>
   //============================================================
   void onSuspend();

   //============================================================
   // <T>执行监视器的逻辑。</T>
   //============================================================
   void execute();
}
