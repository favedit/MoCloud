package org.mo.cloud.core.server;

//============================================================
// <T>服务器控制台接口。</T>
//============================================================
public interface IGcServerConsole
{
   //============================================================
   // <T>获得服务本地地址。</T>
   //
   // @return 本地地址
   //============================================================
   String serviceLocalUrl();

   //============================================================
   // <T>获得服务远程地址。</T>
   //
   // @return 远程地址
   //============================================================
   String serviceRemoteUrl();

   //============================================================
   // <T>生成服务远程地址。</T>
   //
   // @return 远程地址
   //============================================================
   String serviceRemoteUrl(String uri);

   //============================================================
   // <T>获得存储本地地址。</T>
   //
   // @return 本地地址
   //============================================================
   String storageLocalUrl();

   //============================================================
   // <T>获得存储远程地址。</T>
   //
   // @return 远程地址
   //============================================================
   String storageRemoteUrl();

   //============================================================
   // <T>生成存储远程地址。</T>
   //
   // @return 远程地址
   //============================================================
   String storageRemoteUrl(String uri);
}
