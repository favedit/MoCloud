package org.mo.com.system;

//============================================================
// <T>监听器基础类。</T>
//============================================================
public abstract class MListener
      implements
         IListener
{
   // 拥有者
   private Object _owner;

   //============================================================
   // <T>构造监听器基础类。</T>
   //============================================================
   public MListener(){
   }

   //============================================================
   // <T>构造监听器基础类。</T>
   //
   // @param owner 拥有者
   //============================================================
   public MListener(Object owner){
      _owner = owner;
   }

   //============================================================
   // <T>获得拥有者。</T>
   //
   // @return 拥有者
   //============================================================
   public Object owner(){
      return _owner;
   }
}
