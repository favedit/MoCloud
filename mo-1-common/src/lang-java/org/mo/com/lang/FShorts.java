package org.mo.com.lang;

import org.mo.com.lang.generic.MShorts;

//============================================================
// <T>短整数集合。</T>
//============================================================
public class FShorts
      extends MShorts
{
   //============================================================
   // <T>构造短整数集合。</T>
   //============================================================
   public FShorts(){
   }

   //============================================================
   // <T>构造短整数集合。</T>
   //
   // @param capacity 容量
   //============================================================
   public FShorts(int capacity){
      super(capacity);
   }

   //============================================================
   // <T>构造短整数集合。</T>
   //
   // @param data 数据集合
   //============================================================
   public FShorts(short[] data){
      append(data);
   }
}
