package org.mo.com.lang;

//============================================================
// <T>对象集合接口。</T>
//
// @param T 对象类型
//============================================================
public interface ICollection<T>
{
   //============================================================
   // <T>获得数据个数。</T>
   //
   // @return 数据个数
   //============================================================
   int count();

   //============================================================
   // <T>获得索引位置的对象。</T>
   //
   // @param index 索引位置
   // @return 对象
   //============================================================
   T get(int index);

   //============================================================
   // <T>追加一个对象。</T>
   //
   // @param item 对象
   //============================================================
   void push(T item);
}
