package org.mo.com.lang.generic;

//============================================================
// <T>名称值对象叠代器接口。</T>
//============================================================
public interface IPairIterator
{
   //============================================================
   // <T>获得总数。</T>
   //
   // @return 总数
   //============================================================
   int count();

   //============================================================
   // <T>获得位置。</T>
   //
   // @return 位置
   //============================================================
   int position();

   //============================================================
   // <T>判断是否开始位置。</T>
   //
   // @return 是否开始
   //============================================================
   boolean isFirst();

   //============================================================
   // <T>判断是否结束位置。</T>
   //
   // @return 是否结束
   //============================================================
   boolean isLast();
}
