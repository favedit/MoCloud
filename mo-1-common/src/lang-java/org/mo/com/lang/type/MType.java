package org.mo.com.lang.type;

//============================================================
// <T>类型工具。</T>
//
// @history 130219 创建
//============================================================
public class MType
{
   // 是否为空
   protected boolean _isNull = true;

   //============================================================
   // <T>获得是否为空。</T>
   //
   // @return 是否为空
   //============================================================
   public boolean isNull(){
      return _isNull;
   }

   //============================================================
   // <T>重置数据。</T>
   //============================================================
   public void reset(){
      _isNull = false;
   }
}
