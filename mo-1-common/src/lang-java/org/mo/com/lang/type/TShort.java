package org.mo.com.lang.type;

//============================================================
// <T>短整数数据。</T>
//
// @history 130220 创建
//============================================================
public class TShort
      extends MType
{
   // 存储内容
   private short _value;

   //============================================================
   // <T>构造短整数数据。</T>
   //============================================================
   public TShort(){
   }

   //============================================================
   // <T>构造短整数数据。</T>
   //
   // @param value 短整数数据内容
   //============================================================
   public TShort(short value){
      _value = value;
   }

   //============================================================
   // <T>获得短整数数据内容。</T>
   //
   // @return 短整数数据内容
   //============================================================
   public short get(){
      return _value;
   }

   //============================================================
   // <T>设置短整数数据内容。</T>
   //
   // @param value 短整数数据内容
   //============================================================
   public void setValue(short value){
      _value = value;
   }

   //============================================================
   // <T>以字符串方式，获得数据内容。</T>
   //
   // @return 字符串
   //============================================================
   @Override
   public String toString(){
      return Short.toString(_value);
   }
}
