package org.mo.com.lang.type;

//============================================================
// <T>字符数据。</T>
//
// @history 130220 创建
//============================================================
public class TChar
      extends MType
{
   // 存储内容
   protected char _value;

   //============================================================
   // <T>构造字符数据。</T>
   //============================================================
   public TChar(){
   }

   //============================================================
   // <T>构造字符数据。</T>
   //
   // @param value 字节
   //============================================================
   public TChar(char value){
      _value = value;
   }

   //============================================================
   // <T>获得字符数据内容。</T>
   //
   // @return 字符数据内容
   //============================================================
   public char get(){
      return _value;
   }

   //============================================================
   // <T>设置字符数据内容。</T>
   //
   // @param value 字符数据内容
   //============================================================
   public void set(char value){
      _value = value;
   }
}
