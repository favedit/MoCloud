package org.mo.com.lang.type;

//============================================================
// <T>字节数据。</T>
//
// @history 130220 创建
//============================================================
public class TByte
      extends MType
{
   // 存储内容
   protected byte _value;

   //============================================================
   // <T>构造字节数据。</T>
   //============================================================
   public TByte(){
   }

   //============================================================
   // <T>构造字节数据。</T>
   //
   // @param value 字节
   //============================================================
   public TByte(byte value){
      _value = value;
   }

   //============================================================
   // <T>获得数据内容。</T>
   //
   // @return 字节
   //============================================================
   public byte get(){
      return _value;
   }

   //============================================================
   // <T>设置数据内容。</T>
   //
   // @param value 字节
   //============================================================
   public void set(byte value){
      _value = value;
   }
}
