package org.mo.com.lang.type;

//============================================================
// <T>长整数数据。</T>
//
// @history 130220 创建
//============================================================
public class TLong
      extends MType
{
   // 存储内容
   private long _value;

   //============================================================
   // <T>构造长整数数据。</T>
   //============================================================
   public TLong(){
   }

   //============================================================
   // <T>构造长整数数据。</T>
   //
   // @param value 长整数数据内容
   //============================================================
   public TLong(long value){
      _value = value;
   }

   //============================================================
   // <T>获得长整数数据内容。</T>
   //
   // @return 长整数数据内容
   //============================================================
   public void setValue(long value){
      _value = value;
   }

   //============================================================
   // <T>获得长整数数据内容。</T>
   //
   // @param value 长整数数据内容
   //============================================================
   public long value(){
      return _value;
   }

   //============================================================
   // <T>以字符串方式，获得数据内容。</T>
   //
   // @return 字符串
   //============================================================
   @Override
   public String toString(){
      return Long.toString(_value);
   }
}
