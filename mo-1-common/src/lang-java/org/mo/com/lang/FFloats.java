package org.mo.com.lang;

import org.mo.com.lang.generic.MFloats;

//============================================================
// <T>浮点数集合。</T>
//============================================================
public class FFloats
      extends MFloats
{
   //============================================================
   // <T>构造浮点数集合。</T>
   //============================================================
   public FFloats(){
   }

   //============================================================
   // <T>构造浮点数集合。</T>
   //
   // @param capacity 容量
   //============================================================
   public FFloats(int capacity){
      super(capacity);
   }

   //============================================================
   // <T>构造浮点类型集合。</T>
   //
   // @param data 数据集合
   //============================================================
   public FFloats(float[] data){
      append(data);
   }
}
