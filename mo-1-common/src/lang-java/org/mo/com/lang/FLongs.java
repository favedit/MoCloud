package org.mo.com.lang;

import org.mo.com.lang.generic.MLongs;

//============================================================
// <T>长整数集合。</T>
//============================================================
public class FLongs
      extends MLongs
{
   //============================================================
   // <T>构造长整数集合。</T>
   //============================================================
   public FLongs(){
   }

   //============================================================
   // <T>构造长整数集合。</T>
   //
   // @param capacity 容量
   //============================================================
   public FLongs(int capacity){
      super(capacity);
   }

   //============================================================
   // <T>构造长整数集合。</T>
   //
   // @param data 数据集合
   //============================================================
   public FLongs(long[] data){
      append(data);
   }
}
