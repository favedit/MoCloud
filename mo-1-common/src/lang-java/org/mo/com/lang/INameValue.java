package org.mo.com.lang;

//============================================================
// <T>名称内容接口。</T>
//============================================================
public interface INameValue<N, V>
{
   //============================================================
   // <T>根据名称获得内容。</T>
   // <P>不存在则产生例外。</P>
   //
   // @param name 名称
   // @return 内容
   //============================================================
   V get(N name);

   //============================================================
   // <T>根据名称获得内容。</T>
   // <P>不存在则返回默认值。</P>
   //
   // @param name 名称
   // @return 内容
   //============================================================
   V get(N name,
         V defaultValue);

   //============================================================
   // <T>根据名称设置内容。</T>
   //
   // @param name 名称
   // @param value 内容
   //============================================================
   void set(N name,
            V value);
}
