package org.mo.com.lang.reflect;

//============================================================
// <T>描述接口。</T>
//============================================================
public interface IDescriptor
{
   //============================================================
   // <T>根据名称获得类对象。</T>
   //
   // @param name 名称
   // @return 类对象
   //============================================================
   Class<?> type(String name);

   //============================================================
   // <T>获得对象中指定名称的内容。</T>
   //
   // @param name 名称
   // @return 内容
   //============================================================
   Object get(Object item,
              String name);

   //============================================================
   // <T>设置对象中指定名称的内容。</T>
   //
   // @param name 名称
   // @param value 内容
   //============================================================
   void set(Object item,
            String name,
            Object value);
}
