package org.mo.com.lang;

//============================================================
// <T>对照表接口。</T>
//============================================================
public interface IMap<N, V>
      extends
         INameValue<N, V>,
         IDump
{
   //============================================================
   // <T>判断是否为空。</T>
   //
   // @return 是否为空
   //============================================================
   boolean isEmpty();

   //============================================================
   // <T>获得总数。</T>
   //
   // @return 总数
   //============================================================
   int count();

   //============================================================
   // <T>是否存在指定名称。</T>
   //
   // @param name 名称
   // @return 是否存在
   //============================================================
   boolean contains(N name);

   //============================================================
   // <T>接收数据集合到自己内部。</T>
   //
   // @param map 数据集合
   //============================================================
   void assign(IMap<N, V> map);

   //============================================================
   // <T>接收数据集合到自己内部。</T>
   //
   // @param map 数据集合
   // @param offset 开始位置
   // @param count 数据个数
   //============================================================
   void append(IMap<N, V> map);

   //============================================================
   // <T>获得指定索引位置的数据名称。</T>
   //
   // @param index 索引位置
   // @return 数据名称
   //============================================================
   N name(int index);

   //============================================================
   // <T>获得指定索引位置的数据内容。</T>
   //
   // @param index 索引位置
   // @return 数据内容
   //============================================================
   V value(int index);

   //============================================================
   // <T>移除指定名称的数据内容。</T>
   //
   // @param name 数据名称
   // @return 数据内容
   //============================================================
   V remove(N name);

   //============================================================
   // <T>清除全部数据内容。</T>
   //============================================================
   void clear();
}
