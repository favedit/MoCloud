package org.mo.com.lang;

//============================================================
// <T>服务结果枚举。</T>
//============================================================
public enum EResult{
   // 成功
   Success(0),

   // 继续
   Continue(1),

   // 跳过
   Skip(2),

   // 完成
   Finish(3),

   // 失败
   Failure(-1),

   // 取消
   Cancel(-2);

   // 内容
   private int _value;

   //============================================================
   // <T>构造服务结果枚举。</T>
   //============================================================
   private EResult(int value){
      _value = value;
   }

   //============================================================
   // <T>获得内容。</T>
   //
   // @return 内容
   //============================================================
   public int value(){
      return _value;
   }
}
