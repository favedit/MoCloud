package org.mo.com.lang;

import org.mo.com.lang.generic.MChars;

//============================================================
// <T>字符类型集合。</T>
//============================================================
public class FChars
      extends MChars
{
   //============================================================
   // <T>构造字符类型集合。</T>
   //============================================================
   public FChars(){
   }

   //============================================================
   // <T>构造字符类型集合。</T>
   //
   // @param capacity 容量
   //============================================================
   public FChars(int capacity){
      super(capacity);
   }

   //============================================================
   // <T>构造字符类型集合。</T>
   //
   // @param data 数据集合
   //============================================================
   public FChars(char[] data){
      append(data);
   }
}
