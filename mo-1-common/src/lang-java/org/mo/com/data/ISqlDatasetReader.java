package org.mo.com.data;

import java.util.Iterator;
import org.mo.com.collections.FRow;

//============================================================
// <T>数据集合阅读器接口。</T>
//============================================================
public interface ISqlDatasetReader
      extends
         Iterator<FRow>,
         Iterable<FRow>,
         AutoCloseable
{
   //============================================================
   // <T>获得数据描述信息。</T>
   //
   // @return 数据描述信息
   //============================================================
   FSqlDatasetMeta meta();

   //============================================================
   // <T>获得当前索引。</T>
   //
   // @return 索引
   //============================================================
   int currentIndex();

   //============================================================
   // <T>获得当前行数据。</T>
   //
   // @return 行数据
   //============================================================
   FRow currentRow();

   //============================================================
   // <T>关闭阅读器。</T>
   //============================================================
   @Override
   void close();
}
