package org.mo.com.data;

//============================================================
// <T>链接描述。</T>
//
// @author MAOCY
//============================================================
public interface ISqlConnectionMeta
{
   //============================================================
   // <T>列出表格集合。</T>
   //
   // @return 表格集合
   //============================================================
   FSqlTable[] listTables();

   //============================================================
   // <T>找出表格对象。</T>
   //
   // @param name 名称
   // @param detail 详细标志
   // @return 表格对象
   //============================================================
   FSqlTable findTable(String name,
                       boolean detail);

   //============================================================
   // <T>列出视图集合。</T>
   //
   // @return 视图集合
   //============================================================
   FSqlView[] listViews();

   //============================================================
   // <T>找出表格对象。</T>
   //
   // @param name 名称
   // @param detail 详细标志
   // @return 表格对象
   //============================================================
   FSqlView findView(String name,
                     boolean detail);
}
