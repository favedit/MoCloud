package org.mo.com.message;

import org.mo.com.xml.FXmlNode;

//============================================================
// <T>消息接口。</T>
//============================================================
public interface IMessage
{
   //============================================================
   // <T>获得名称。</T>
   //
   // @return 名称
   //============================================================
   String name();

   //============================================================
   // <T>获得类型。</T>
   //
   // @return 类型
   //============================================================
   String type();

   //============================================================
   // <T>获得描述。</T>
   //
   // @return 描述
   //============================================================
   String description();

   //============================================================
   // <T>获得消息。</T>
   //
   // @return 消息
   //============================================================
   String message();

   //============================================================
   // <T>获得参数。</T>
   //
   // @return 参数
   //============================================================
   Object[] params();

   //============================================================
   // <T>加载配置信息。</T>
   //
   // @param config 配置信息
   //============================================================
   void loadConfig(FXmlNode config);

   //============================================================
   // <T>保存配置信息。</T>
   //
   // @param config 配置信息
   //============================================================
   void saveConfig(FXmlNode config);

   //============================================================
   // <T>将当前内容转换为一个配置节点。</T>
   //
   // @return 配置节点
   //============================================================
   FXmlNode convertToNode();
}
