package org.mo.com.xml;

//============================================================
// <T>XML对象集合的接口。</T>
//
// @author MAOCY
// @version 1.00 - 2008/12/08
//============================================================
public interface IXmlObjects
      extends
         Iterable<IXmlObject>
{
   //============================================================
   // <T>获得总数。</T>
   //
   // @return 总数
   //============================================================
   int count();

   //============================================================
   // <T>查找指定对象的索引位置。</T>
   //
   // @return 索引位置
   //============================================================
   int indexOf(IXmlObject xobject);

   //============================================================
   // <T>根据参数集合查找节点。</T>
   //
   // @param values 参数集合
   // @return 节点
   //============================================================
   IXmlObject find(String... values);

   //============================================================
   // <T>根据索引位置获得节点。</T>
   //
   // @param index 索引位置
   // @return 节点
   //============================================================
   IXmlObject get(int index);

   //============================================================
   // <T>根据对象编号获得节点。</T>
   //
   // @param objectId 对象编号
   // @return 节点
   //============================================================
   IXmlObject get(String objectId);

   //============================================================
   // <T>根据索引位置设置节点。</T>
   //
   // @param index 索引位置
   // @param xobject 节点
   //============================================================
   void set(int index,
            IXmlObject xobject);

   //============================================================
   // <T>推入入一个节点到尾部。</T>
   //
   // @param xobject 节点
   //============================================================
   void push(IXmlObject xobject);

   //============================================================
   // <T>根据对象编号移除节点。</T>
   //
   // @param objectId 对象编号
   // @return 节点
   //============================================================
   IXmlObject remove(String objectId);

   //============================================================
   // <T>根据对象编号查找节点。</T>
   //
   // @param objectId 对象编号
   // @return 节点
   //============================================================
   IXmlObject search(String objectId);

   //============================================================
   // <T>清空处理。</T>
   //============================================================
   void clear();
}
