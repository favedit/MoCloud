package org.mo.com.text.parser;

import org.mo.com.lang.FString;

//============================================================
// <T>运行信息。</T>
//============================================================
public class FTextSource
      extends FString
{
   // 级别
   protected int _level;

   //============================================================
   // <T>构造运行信息。</T>
   //============================================================
   public FTextSource(){
   }

   //============================================================
   // <T>获得递归级别。</T>
   //
   // @return 递归级别
   //============================================================
   public int level(){
      return _level;
   }

   //============================================================
   // <T>增加递归级别。</T>
   //============================================================
   public void increase(){
      _level++;
   }

   //============================================================
   // <T>减少递归级别。</T>
   //============================================================
   public void decrease(){
      _level--;
   }

   //============================================================
   // <T>追加迭代信息。</T>
   //============================================================
   public void appendIndent(){
      int n = -1;
      while(++n < _level){
         append("   ");
      }
   }
}
