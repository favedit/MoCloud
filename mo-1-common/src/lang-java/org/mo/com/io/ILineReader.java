package org.mo.com.io;

//============================================================
// <T>字符串行读取接口。</T>
//
//@type T 对象类型
//============================================================
public interface ILineReader
{
   //============================================================
   // <T>判断是否有下一行字符串。</T>
   //
   // @return 是否有
   //============================================================
   boolean hasNext();

   //============================================================
   // <T>>获得下一行字符串。</T>
   //
   // @return 字符串
   //============================================================
   String readLine();
}
